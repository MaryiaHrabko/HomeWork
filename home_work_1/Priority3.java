package home_work_1;

public class Priority3 {
    public static void main(String[] args) {
        int a = 5;
        int b = 2;
        int x = 2;
        int c = 8;
        int d = (a + b++) / c;
        //складываем 5 и 2. Получаем 7.
        // ++ в данном случае игнорируем так как это пост-инкремент, +1 добавилось бы при следующем использовании переменной b.
        // 7/8=0,875. Int отбрасывает дробную часть 0,875. Результат 0.

        double e = (a + 1.0 * x++) / c;
        //складываем 5 и 2 (умноженное на 1.0 чтобы получить дробный тип). Получаем 7.
        // ++ в данном случае игнорируем так как это пост-инкремент, +1 добавилось бы при следующем использовании переменной x.
        // 7/8=0,875.

        System.out.println("d = " + d);
        System.out.println("e = " + e);
    }
}
